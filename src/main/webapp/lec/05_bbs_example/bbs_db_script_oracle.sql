-- article_replay

CREATE TABLE ARTICLE_REPLY (
	ARTICLE_ID		  INT	REFERENCES ARTICLE( article_id ) ,
	REPLY_USER_ID 	INT REFERENCES users ( user_id ) ,
	REPLY_ID 		    INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
