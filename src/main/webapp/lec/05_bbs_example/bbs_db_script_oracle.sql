-- system properties

CREATE TABLE SYS_PROP (
	prop_key  VARCHAR2(255) ,
	VALUE     VARCHAR2(255) ,
	value_int INT ,
	value_dbl NUMBER ,
	PRIMARY KEY ( prop_KEY, VALUE )
) ;

-- code table

CREATE TABLE CODE (
	p_code_id   VARCHAR2(255) ,
	code_id     VARCHAR2(255) PRIMARY KEY ,
	text_value  VARCHAR2(255) ,
	num_value   NUMBER ,
	ord INT
) ;

-- users

CREATE TABLE USERS (
	user_id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,
	user_name      VARCHAR2(255) NOT NULL,
	email          VARCHAR2(255) NOT NULL ,
	passwd         VARCHAR2(255) ,
	phone_no       VARCHAR2(255) ,
	last_login_dt  TIMESTAMP ,
	last_logout_dt TIMESTAMP ,
	role_code_id   VARCHAR2(255) DEFAULT 1 NOT NULL
) ;

